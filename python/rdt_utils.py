def dfs(obj: list|dict, x):
    for key in obj:
        val = key if type(obj) == list else obj[key]
        if x == key:
            return val
        if type(val) not in [dict, list]:
            continue
        res = dfs(val, x)
        if res is not None:
            return res

def dfs_path(obj: list|dict, x, path:list=[]):
    for i, key in enumerate(obj):
        val = key if type(obj) == list else obj[key]
        if x == key:
            return path + [key if type(obj)==dict else i]
        if type(val) not in [dict, list]:
            continue
        res = dfs_path(val, x, path + [key if type(obj)==dict else i])
        if res is not None:
            return path + [key if type(obj)==dict else i]

