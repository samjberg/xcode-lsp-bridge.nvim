def bfs_dict(dct: dict, key):
    '''Performs a breadth first search in `dct` for `key`, returning the first result found, otherwise None.
       Automatically handles both dicts and lists as subitems, but `dct` itself must be a dict.
    '''
    for k, val in dct.items():
        if k == key:
            return val
        if isinstance(val, dict):
            res = bfs_dict(val, key)
            if res:
                return res
        elif isinstance(val, list):
            for entry in val:
                if isinstance(entry, dict):
                    res = bfs_dict(entry, key)
                    if res:
                        return res





