def ensure_drive_letter(path: str, drive_letter='C') -> str:
    drive_prefix = drive_letter + ':'
    if path.startswith(drive_prefix):
        return path
    path = path if (path.startswith('/') or path.startswith('\\')) else '/' + path
    return drive_prefix + path

def normalize_path(path: str, sep='/', strip_drive=False) -> str:
    if isinstance(path, int):
        log_unmissable(f'NORMALIZE_PATH RECEIEVED INT: {str(path)}')
    if strip_drive:
        path = os.path.splitdrive(path)[1]
    if sep == '/':
        return path.replace('\\', '/')
    elif sep == '\\':
        return path.replace('/', '\\')
    raise RuntimeError(f"Invalid separator, must be '/' or '\\'.  Offending path: {path}")

